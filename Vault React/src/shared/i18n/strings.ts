export function t(key: string, variables?: Record<string, string | number>): string {
  let value = STRINGS[key] ?? key;

  if (variables) {
    for (const [name, replacement] of Object.entries(variables)) {
      value = value.replaceAll(`{${name}}`, String(replacement));
    }
  }

  return value;
}

export function formatString(templateKey: string, ...values: (string | number)[]): string {
  const template = STRINGS[templateKey] ?? templateKey;

  return values.reduce<string>((result, value, index) => {
    const token = `%${index + 1}$s`;
    return result.replaceAll(token, String(value));
  }, template);
}
